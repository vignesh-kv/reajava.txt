class ComparisonTool {
    public static void main(String[] args) {
        int a = 1;
        int b = 1;

        
        if (a == b) {
            System.out.println("a == b is true");
        }
        if (a >= b) {
            System.out.println("a >= b is true");
        }
        if (a < b) {
            System.out.println("a < b is true");
        }
        if (a <= b) {
            System.out.println("a <= b is true");
        }
        if (a != b) {
            System.out.println("a != b is true");
        } else {
            System.out.println("invalid");
        }
    }
}
